package likey.likeyVO;

public class LikeyVO {

	private int l_no;
	private int l_pno;
	private String l_id;
	private int l_rno;
	public int getL_no() {
		return l_no;
	}
	public void setL_no(int l_no) {
		this.l_no = l_no;
	}
	public int getL_pno() {
		return l_pno;
	}
	public void setL_pno(int l_pno) {
		this.l_pno = l_pno;
	}
	public String getL_id() {
		return l_id;
	}
	public void setL_id(String l_id) {
		this.l_id = l_id;
	}
	public int getL_rno() {
		return l_rno;
	}
	public void setL_rno(int l_rno) {
		this.l_rno = l_rno;
	}
	
	
	public LikeyVO(int l_no, int l_pno, String l_id, int l_rno) {
		super();
		this.l_no = l_no;
		this.l_pno = l_pno;
		this.l_id = l_id;
		this.l_rno = l_rno;
	}
	
	
	
	
	
}
